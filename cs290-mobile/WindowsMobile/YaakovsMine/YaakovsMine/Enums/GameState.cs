namespace YaakovsMine
{
    /// <summary>
    /// Enumerates possible game states.
    /// </summary>
    public enum GameState
    {
        /// <summary>
        /// Indicates that a game has not yet been started.
        /// </summary>
        NotStarted,

        /// <summary>
        /// Indicates that a game is currently in progress.
        /// </summary>
        Playing,

        /// <summary>
        /// Indicates that the game has ended.
        /// </summary>
        Ended
    }
}
