namespace YaakovsMine
{
    /// <summary>
    /// Enumerates possible types of falling objects.
    /// </summary>
    public enum FallingObject
    {
        /// <summary>
        /// Indicates that the falling object is a rock.
        /// </summary>
        Rock,

        /// <summary>
        /// Indicates that the falling object is a box with a nitroglycerin vial inside.
        /// </summary>
        Nitroglycerin,

        /// <summary>
        /// Indicates that the falling object is a pickaxe.
        /// </summary>
        Pickaxe,

        /// <summary>
        /// Indicates that the falling object is a medical kit.
        /// </summary>
        MedicalKit
    }
}
