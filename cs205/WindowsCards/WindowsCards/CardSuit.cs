namespace Petroules.WindowsCards
{
    /// <summary>
    /// Enumerates the four card suits.
    /// </summary>
    public enum CardSuit
    {
        /// <summary>
        /// Indicates the clubs suit.
        /// </summary>
        Clubs = 0,

        /// <summary>
        /// Indicates the diamonds suit.
        /// </summary>
        Diamonds = 1,

        /// <summary>
        /// Indicates the hearts suit.
        /// </summary>
        Hearts = 2,

        /// <summary>
        /// Indicates the spades suit.
        /// </summary>
        Spades = 3
    }
}
