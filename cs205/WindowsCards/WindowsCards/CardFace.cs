namespace Petroules.WindowsCards
{
    /// <summary>
    /// Enumerates the thirteen card faces.
    /// </summary>
    public enum CardFace
    {
        /// <summary>
        /// Represents an ace card (face value of 1).
        /// </summary>
        Ace = 0,

        /// <summary>
        /// Represents a card with a face value of 2.
        /// </summary>
        Two = 1,

        /// <summary>
        /// Represents a card with a face value of 3.
        /// </summary>
        Three = 2,

        /// <summary>
        /// Represents a card with a face value of 4.
        /// </summary>
        Four = 3,

        /// <summary>
        /// Represents a card with a face value of 5.
        /// </summary>
        Five = 4,

        /// <summary>
        /// Represents a card with a face value of 6.
        /// </summary>
        Six = 5,

        /// <summary>
        /// Represents a card with a face value of 7.
        /// </summary>
        Seven = 6,

        /// <summary>
        /// Represents a card with a face value of 8.
        /// </summary>
        Eight = 7,

        /// <summary>
        /// Represents a card with a face value of 9.
        /// </summary>
        Nine = 8,

        /// <summary>
        /// Represents a card with a face value of 10.
        /// </summary>
        Ten = 9,

        /// <summary>
        /// Represents a jack card (face value of 11).
        /// </summary>
        Jack = 10,

        /// <summary>
        /// Represents a queen card (face value of 12).
        /// </summary>
        Queen = 11,

        /// <summary>
        /// Represents a king card (face value of 13).
        /// </summary>
        King = 12
    }
}
